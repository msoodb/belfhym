#include "blfm_led.h"
#include "stm32f1xx.h"

void blfm_led_init(void) {
    GPIOC->CRH &= ~(GPIO_CRH_MODE13 | GPIO_CRH_CNF13);
    GPIOC->CRH |= GPIO_CRH_MODE13_0;
}

void blfm_led_toggle(void) { GPIOC->ODR ^= GPIO_ODR_ODR13; }
